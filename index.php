                <h1>Recommendation 1:</h1>
                
                <p>
                There was currently no distinction between the School and District links, causing confusion with seemingly redundant and unnecessary links.  We recommend distinct headers announcing which navigation bar is associated with either the School or the District. ("Before" screen shots on left. "After" screen shots on right)    
                </p>
                
                
                <img src="5.png" alt="Screenshot of before side bar" width="300">
                <img src="4.png" alt="Screenshot of after side bar" width="300">
                <img src="6.png" alt="Screenshot of before top bar" height="50">
                <img src="7.png" alt="Screenshot of after top bar" height="50">
                
                <h1>Recommendation 2:</h1>
                <p>
                Wellness was a difficult link to find, buried in the depths of the Staff tab on the District tab.  This should be removed from there and placed on the main District navigation bar for easier accessibility. Wellness is something that should be easily and quickly accessed, as a student, parent, or staff member.    
                </p>
                <img src="1.png" alt="Screenshot of nav bars." width="600">
                
                <h1>Recommendation 3:</h1>
                <p>
                There was an overwhelming number of links on the School navigation bar, a fact every usability tester pointed out.  To revise this issue, Administration should be moved into the Staff tab, IAA Newsletter and In the News were moved into News and Announcements, and the Integrated Arts 2014-2015 should be removed as it is a redundant link to Announcements.    
                </p>
                <img src="3.png" alt="Screenshot of side nav" width="600">
                
                <h1>Recommendation 4:</h1>
                <p>
                There was much confusion over what links would bring the user to an outside source, causing disorientation when the entire layout changed.  To enhance the user experience, outside links should be changed to a different color or have some other indicator that they are outside links. As an example, and as shown in our screenshot, we changed the outside links to a gold color, to display links that are different from the internal site links.
                </p>
                
                <img src="7.png" alt="Screenshot or top nav" height="50">
                <h1>Recommendation 5:</h1>
                <p>
                The site had an overwhelming number of paragraphs and articles on the first page, causing the user to scroll a reasonable amount through what was called “fluff.”  To fix the issue, half of the words should be removed, and then half of those, leaving a manageable amount on the page. Spacing and prominent headings should also added between paragraphs for easier reading.    
                </p>
                
